//------------------------------------------------------------------------------------------
// Random Number Between
//------------------------------------------------------------------------------------------

export function randomNumberBetween(min, max) {
    return Math.floor(Math.random() * (max - min + 1) + min)
}

//------------------------------------------------------------------------------------------
// Sleep (Pause)
//------------------------------------------------------------------------------------------

export function sleep(duration) {
    return new Promise(resolve => {
        setTimeout(resolve, duration)
    })
}